package com.StringBufferAndStringBuilderMethods;

public class IndexOf {

	public static void main(String[] args) {
		
		StringBuffer name1 = new StringBuffer("Welcome to India");
		
		System.out.println(name1.indexOf("Welcome"));
		
		System.out.println(name1.indexOf("to"));
		
		System.out.println(name1.indexOf("India"));
		
	}

}
